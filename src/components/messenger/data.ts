export function getMuskaResponse(text: string): string {
  const lower = text.toLowerCase();
  
  for (const [category, keywords] of Object.entries(MUSKA_KNOWLEDGE)) {
    if (keywords.some(kw => lower.includes(kw))) {
      const responses = MUSKA_RESPONSES[category] || MUSKA_RESPONSES.default;
      return responses[Math.floor(Math.random() * responses.length)];
    }
  }
  
  return MUSKA_RESPONSES.default[Math.floor(Math.random() * MUSKA_RESPONSES.default.length)];
}

